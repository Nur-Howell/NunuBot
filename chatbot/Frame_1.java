    private void ThemeChangerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemeChangerActionPerformed
        
        if (isDarkTheme) {
        // Switch to Light Theme
        getContentPane().setBackground(Color.WHITE);
        textArea1.setBackground(Color.WHITE);
        textArea1.setForeground(Color.BLACK);
        textField1.setBackground(Color.WHITE);
        textField1.setForeground(Color.BLACK);
        
        isDarkTheme = false; // Update the state
    } else {
        // Switch back to Original Dark Theme
        getContentPane().setBackground(Color.DARK_GRAY);
        textArea1.setBackground(Color.DARK_GRAY);
        textArea1.setForeground(Color.WHITE); 
        textField1.setBackground(Color.DARK_GRAY);
        textField1.setForeground(Color.WHITE);
        
        isDarkTheme = true; // Update the state
    }
    }//GEN-LAST:event_ThemeChangerActionPerformed

