    private void textField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textField1ActionPerformed
       
        String input = textField1.getText().trim();
    if (input.isEmpty()) return;

    // Show what the user typed
    textArea1.append("You: " + input + "\n");
    textField1.setText(""); // clear the input field

    // Handle exit
    if (input.equalsIgnoreCase("exit")) {
        textArea1.append("Bot: Goodbye, " + user.name + ". Shutting down...\n");
        textField1.setEnabled(false);
        
        javax.swing.Timer timer = new javax.swing.Timer(2000, e -> System.exit(0));
        timer.setRepeats(false); 
        timer.start();
        
        return;
    }

    // Pass to bot logic
    user.lastInput = input;
    user.updateScore();

    String reply = bot.processMessage(input);
   
    if (reply.equals("COMMAND_CLEAR")) {
    textArea1.setText("Bot: Hello! Type a message below and press Enter. \n\n");
    } else {
    
    textArea1.append("Bot: " + reply + "\n");
    textArea1.append("(Score: " + user.conversationScore + ")\n\n");
}
    
        
    }//GEN-LAST:event_textField1ActionPerformed

